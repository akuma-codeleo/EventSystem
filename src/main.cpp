#include <iostream>
#include <GLFW/glfw3.h>
#include "Core/window.h"

int main()
{
    Core::Window& window = Core::Window::getInstanse();

    while (!glfwWindowShouldClose(window.getGLFWwindow()))
    {
        glfwPollEvents();
        glfwSwapBuffers(window.getGLFWwindow());
    }

    return 0;
}